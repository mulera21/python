def add(a, b):
    print(f"addition {a} + {b}")
    return a + b

def subtraction(a, b):
    print(f"subtracting {a} - {b}")
    return a - b
def multiplication(a, b):
    print(f"multiplying {a} * {b}")
    return a * b

print("lets do some maths with function")
age = add(30, 40)
height = subtraction(30, 10)
weight = multiplication(5, 10)
print(f" AGE: {age}, HEIGHT: {height}, weight: {weight}")

